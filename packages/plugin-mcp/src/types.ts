import type { McpServer, ResourceTemplate } from '@modelcontextprotocol/server'
import type {
  CollectionConfig,
  CollectionSlug,
  GlobalSlug,
  PayloadRequest,
  TypedUser,
} from 'payload'

/**
 * A JSON Schema describing tool / prompt input parameters. The plugin no longer
 * accepts zod schemas directly — bring your own zod (or any validator) and produce
 * a plain JSON Schema object (e.g. via `z.toJSONSchema(schema)` in zod 4).
 */
export type MCPInputSchema = JsonSchemaObject

/**
 * Minimal JSON Schema shape used internally. Intentionally permissive — we don't
 * try to model every keyword, since the schema is consumed by `fromJsonSchema()`
 * and validated by the runtime validator (`@cfworker/json-schema`).
 *
 * @internal
 */
export type JsonSchemaObject = {
  $ref?: string
  $schema?: string
  [key: string]: unknown
  additionalProperties?: boolean | JsonSchemaObject
  allOf?: JsonSchemaObject[]
  anyOf?: JsonSchemaObject[]
  default?: unknown
  definitions?: Record<string, JsonSchemaObject>
  description?: string
  enum?: unknown[]
  format?: string
  items?: JsonSchemaObject | JsonSchemaObject[]
  maximum?: number
  maxLength?: number
  minimum?: number
  minLength?: number
  oneOf?: JsonSchemaObject[]
  pattern?: string
  properties?: Record<string, JsonSchemaObject>
  required?: string[]
  type?: string | string[]
}

export type MCPPluginConfig = {
  /**
   * Per-collection MCP overrides. Every collection in your Payload config is exposed
   * via MCP by default (with all four operations: create / find / update / delete) —
   * use this map to opt-out specific collections or operations and to attach
   * descriptions / response overrides.
   *
   * @example
   * ```ts
   * collections: {
   *   posts: { description: 'Blog posts' },          // exposed, all ops
   *   secrets: { disabled: true },                   // not exposed at all
   *   users: { disabled: { delete: true } },         // delete blocked, others enabled
   * }
   * ```
   */
  collections?: Partial<
    Record<
      CollectionSlug,
      {
        /**
         * Set the description of the collection. This is used by MCP clients to determine when to use the collecton as a resource.
         */
        description?: string
        /**
         * Disable the whole collection (`true`) or specific operations. Anything not
         * listed here remains enabled.
         */
        disabled?:
          | {
              create?: boolean
              delete?: boolean
              find?: boolean
              update?: boolean
            }
          | boolean

        /**
         * Override the response generated by the MCP client. This allows you to modify the response that is sent to the MCP client. This is useful for adding additional data to the response, data normalization, or verifying data.
         */
        overrideResponse?: (
          response: {
            content: Array<{
              text: string
              type: string
            }>
          },
          doc: Record<string, unknown>,
          req: PayloadRequest,
        ) => {
          content: Array<{
            text: string
            type: string
          }>
        }
      }
    >
  >
  /**
   * Disable the MCP plugin.
   */
  disabled?: boolean
  /**
   * Experimental features
   * **These features are for experimental purposes -- They are Disabled in Production by Default**
   */
  experimental?: {
    /**
     * These are MCP tools that can be used by a client to operate on Payload.
     */
    tools: {
      /**
       * **Experimental** -- Auth MCP tools allow a client to exercise authentication operations
       * (login, verify, reset / forgot password, unlock). They do not modify any files.
       */
      auth?: {
        /**
         * Enable the auth MCP tools.
         * @default false
         */
        enabled: boolean
      }
    }
  }
  /**
   * Per-global MCP overrides. Every global in your Payload config is exposed via MCP
   * by default (with `find` and `update` — globals are singletons, so there's no
   * create / delete) — use this map to opt-out specific globals or operations and to
   * attach descriptions / response overrides.
   */
  globals?: Partial<
    Record<
      GlobalSlug,
      {
        /**
         * Set the description of the global. This is used by MCP clients to determine when to use the global as a resource.
         */
        description?: string
        /**
         * Disable the whole global (`true`) or specific operations. Anything not
         * listed here remains enabled.
         */
        disabled?:
          | {
              find?: boolean
              update?: boolean
            }
          | boolean

        /**
         * Override the response generated by the MCP client. This allows you to modify the response that is sent to the MCP client. This is useful for adding additional data to the response, data normalization, or verifying data.
         */
        overrideResponse?: (
          response: {
            content: Array<{
              text: string
              type: string
            }>
          },
          doc: Record<string, unknown>,
          req: PayloadRequest,
        ) => {
          content: Array<{
            text: string
            type: string
          }>
        }
      }
    >
  >
  /**
   * MCP Server options.
   */
  mcp?: {
    /**
     * Add custom MCP Prompts.
     */
    prompts?: {
      /**
       * Set the args schema of the prompt. This is the args schema that will be passed to the prompt. This is used by MCP clients to determine the arguments that will be passed to the prompt.
       */
      argsSchema: MCPInputSchema
      /**
       * Set the description of the prompt. This is used by MCP clients to determine when to use the prompt.
       */
      description: string
      /**
       * Set the handler of the prompt. This is the function that will be called when the prompt is used.
       */
      handler: (
        args: Record<string, unknown>,
        req: PayloadRequest,
        _extra: unknown,
      ) =>
        | {
            messages: Array<{
              content: {
                text: string
                type: 'text'
              }
              role: 'assistant' | 'user'
            }>
          }
        | Promise<{
            messages: Array<{
              content: {
                text: string
                type: 'text'
              }
              role: 'assistant' | 'user'
            }>
          }>
      /**
       * Set the function name of the prompt.
       */
      name: string
      /**
       * Set the title of the prompt. LLMs will interperate the title to determine when to use the prompt.
       */
      title: string
    }[]

    /**
     * Add custom MCP Resource.
     */
    resources?: {
      /**
       * Set the description of the resource. This is used by MCP clients to determine when to use the resource.
       * example: 'Data is a resource that contains special data.'
       */
      description: string
      /**
       * Set the handler of the resource. This is the function that will be called when the resource is used.
       * The handler can have either 3 arguments (when no args are passed) or 4 arguments (when args are passed).
       */
      handler: (...args: any[]) =>
        | {
            contents: Array<{
              text: string
              uri: string
            }>
          }
        | Promise<{
            contents: Array<{
              text: string
              uri: string
            }>
          }>
      /**
       * Set the mime type of the resource.
       * example: 'text/plain'
       */
      mimeType: string
      /**
       * Set the function name of the resource.
       * example: 'data'
       */
      name: string
      /**
       * Set the title of the resource. LLMs will interperate the title to determine when to use the resource.
       * example: 'Data'
       */
      title: string
      /**
       * Set the uri of the resource.
       * example: 'data://app'
       */
      uri: ResourceTemplate | string
    }[]

    serverOptions?: MCPServerOptions
    /**
     * Add custom MCP Tools.
     */
    tools?: {
      /**
       * Set the description of the tool. This is used by MCP clients to determine when to use the tool.
       */
      description: string
      /**
       * Set the handler of the tool. This is the function that will be called when the tool is used.
       */
      handler: (
        args: Record<string, unknown>,
        req: PayloadRequest,
        _extra: unknown,
      ) =>
        | {
            content: Array<{
              text: string
              type: 'text'
            }>
            role?: string
          }
        | Promise<{
            content: Array<{
              text: string
              type: 'text'
            }>
            role?: string
          }>
      /**
       * Set the name of the tool. This is the name that will be used to identify the tool. LLMs will interperate the name to determine when to use the tool.
       */
      name: string
      /**
       * Set the parameters of the tool. This is the parameters that will be passed to the tool.
       */
      parameters: MCPInputSchema
    }[]
    /**
     * Set verbose logging.
     * @default false
     */
    verboseLogs?: boolean
  }

  /**
   * Override the API key collection.
   * This allows you to add fields to the API key collection or modify the collection in any way you want.
   * @param collection - The API key collection.
   * @returns The modified API key collection.
   */
  overrideApiKeyCollection?: (collection: CollectionConfig) => CollectionConfig

  /**
   * Override the authentication method.
   * This allows you to use a custom authentication method instead of the default API key authentication.
   * @param req - The request object.
   * @returns The MCP access settings.
   */
  overrideAuth?: (
    req: PayloadRequest,
    getDefaultMCPAccess: (overrideApiKey?: null | string) => Promise<MCPAccess>,
  ) => MCPAccess | Promise<MCPAccess>

  /**
   * Set the users collection that API keys should be associated with.
   */
  userCollection?: CollectionSlug
}

/**
 * Configure the arguments passed to the MCPServer constructor.
 */
export type MCPServerOptions = {
  /**
   * Server options passed as the second argument to the MCP Server constructor.
   *
   * You can use it to pass instructions describing how to use the server and its features.
   */
  options?: ConstructorParameters<typeof McpServer>[1]
  /**
   * Server info passed as the first argument to the MCP Server constructor.
   */
  serverInfo?: Partial<ConstructorParameters<typeof McpServer>[0]>
}

export type MCPAccess = {
  auth?: {
    auth?: boolean
    forgotPassword?: boolean
    login?: boolean
    resetPassword?: boolean
    unlock?: boolean
    verify?: boolean
  }
  custom?: Record<string, boolean>
  /**
   * This is passed to all local API calls.
   */
  overrideAccess: boolean
  'payload-mcp-prompt'?: Record<string, boolean>
  'payload-mcp-resource'?: Record<string, boolean>
  'payload-mcp-tool'?: Record<string, boolean>
  /**
   * The user associated with the API key, passed to local API calls
   */
  user: null | TypedUser
} & Record<string, unknown>

export type EntityConfig = MCPPluginConfig['collections'] | MCPPluginConfig['globals']
